function finalWP=NewWayPoint(p)
% 新思路，给定三个路径点，求这三个路径点的外接圆的圆心，即为该场景的路径点


cen1=(p(1,:)+p(2,:))/2;         %三角形一条边中点
cen2=(p(2,:)+p(3,:))/2;         %另一条边中点

k1=-1/((p(1,2)-p(2,2))/(p(1,1)-p(2,1)));    %一条边垂直平分线
b1=cen1(2)-k1*cen1(1);

k2=-1/((p(2,2)-p(3,2))/(p(2,1)-p(3,1)));    %另一条边垂直平分线
b2=cen2(2)-k2*cen2(1);

x0=-(b1-b2)/(k1-k2);             %求两直线交点
y0=-(-b2*k1+b1*k2)/(k1-k2);
                    
finalWP=[x0,y0];

% r=sqrt((x0-p(1,1))^2+(y0-p(1,2))^2);  %求外接圆半径，暂时不需要


end

